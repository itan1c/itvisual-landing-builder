function translateDOM(dict) {
    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
    let node;
    while (node = walker.nextNode()) {
        const text = node.nodeValue.trim();
        if (text && dict[text]) {
            node.nodeValue = node.nodeValue.replace(text, dict[text]);
        }
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const lang = localStorage.getItem('ve_lang') || 'uk';
    if (lang === 'ru') {
        translateDOM(UI_RU);
    } else {
        translateDOM(UI_UK);
    }

    // Auto-translate dynamically added nodes (like projects)
    const observer = new MutationObserver((mutations) => {
        mutations.forEach((mutation) => {
            if (mutation.addedNodes.length) {
                mutation.addedNodes.forEach(nn => {
                    if (nn.nodeType === 1 && !nn.classList?.contains('__sel')) {
                        const walker = document.createTreeWalker(nn, NodeFilter.SHOW_TEXT, null, false);
                        let n;
                        while (n = walker.nextNode()) {
                            const t = n.nodeValue.trim();
                            if (t && (lang === 'ru' ? UI_RU[t] : UI_UK[t])) n.nodeValue = n.nodeValue.replace(t, (lang === 'ru' ? UI_RU[t] : UI_UK[t]));
                        }
                    }
                });
            }
        });
    });
    observer.observe(document.body, { childList: true, subtree: true });
});
